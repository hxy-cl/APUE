#ifndef __POLL_H                // 头文件保护宏，防止重复包含
#define __POLL_H

// 宏定义 buf 空间的大小（字节）
#define BUFSIZE 1024

// 定义有限状态机的四种状态
// STATE_R: 读状态，从读文件描述符读取数据
// STATE_W: 写状态，将数据写入写文件描述符
// STATE_E: 错误状态，发生错误时打印错误信息
// STATE_T: 终止状态，结束处理
enum {STATE_R, STATE_W, STATE_E, STATE_T};

// 定义有限状态机的数据类型
typedef struct
{   
    int rfd;            // 读文件的文件描述符（源）
    int wfd;            // 写文件的文件描述符（目标）
    char buf[BUFSIZE];  // 缓冲区，存储读取到的数据
    int count;          // 成功读取到的字节个数（待写入的数据量）
    int pos;            // 已写入的字节数（用于续写）
    int state;          // 有限状态机当前的状态（R/W/E/T）
    char *errmsg;       // 记录出错函数的函数名（用于 perror）
} fsm_t;

// 函数接口声明

/*
功能 : 初始化有限状态机
参数 : f    - 指向 fsm_t 指针的指针，用于回填创建的状态机对象
       rfd  - 读文件的文件描述符（客户指定，需已打开）
       wfd  - 写文件的文件描述符（客户指定，需已打开）
返回值 : 成功返回 0；失败返回 -1（内存分配失败）或 -2（其他错误）
说明 : 为状态机动态分配内存，初始化成员，并将两个文件描述符设置为非阻塞模式。
       客户需确保传递的 rfd 和 wfd 是有效的。
*/
extern int fsm_init(fsm_t **f, int rfd, int wfd);

/*
功能 : 推动有限状态机执行一步
参数 : f - 指向要推动的有限状态机
返回值 : 始终返回 0（当前实现未使用返回值）
说明 : 根据当前状态执行相应操作：
       - R状态：从 rfd 读取数据到 buf，根据结果切换到 W/E/T 状态
       - W状态：将 buf 中的数据写入 wfd，支持续写，写完切回 R 状态
       - E状态：打印错误信息，切换到 T 状态
       - T状态：不做任何操作（由外部决定是否结束进程）
*/
extern int fsm_drive(fsm_t *f);

/*
功能 : 销毁有限状态机，释放内存
参数 : f - 指向要销毁的有限状态机
返回值 : 成功返回 0，失败返回 <0（当前实现总是返回0）
说明 : 释放 f 指向的内存，并将 f 置为 NULL（注意：传入的是指针值，需调用者自己置 NULL）
*/
extern int fsm_destroy(fsm_t *f);

#endif   // __POLL_H 结束头文件保护
